       ALLOCATE(GX(NGXMAX),GY(NGXMAX),GY2(NGXMAX))
       DXPOS=(RX2-RX1)/(NGXMAX-1)
       CRF0=DCMPLX(RF0,0.D0)
       CKX0=RKX0
       CKY0=RKY0
       CKZ0=RKZ0
       YPOS0=RY0
       ZPOS0=RZ0
       DO NX=1,NGXMAX
          ILIST=0
          XPOS0=RX1+DXPOS*(NX-1)
          CALL DPBRENT2(CKX)
          GX(NX)=GUCLIP(DBLE(XPOS0))
          GY(NX)=GUCLIP(DBLE(CKX**2))
