  SUBROUTINE DPGRP0

    USE dpcomm_local
    USE dpdisp
    USE dpfpin,ONLY: dpldfm
    IMPLICIT NONE
    COMPLEX(rkind):: CRF,CKX,CKY,CKZ,CD0,CD1,CD2,CD3,CD4,CD5,CD6
    REAL(rkind):: RF0_SAVE
    INTEGER:: MODELP_SAVE(NSMAX),MODELV_SAVE(NSMAX),NS,IERR

1   WRITE(6,'(A)')          '## INPUT: RF0,RFI0,RKX0,RKY0,RKZ0,RX0,RY0,RZ0='
    WRITE(6,'(A,1P2E12.4)') '##   ',RF0,RFI0
    WRITE(6,'(A,1P6E12.4)') '##   ',RKX0,RKY0,RKZ0,RX0,RY0,RZ0
    RF0_SAVE=RF0
    READ(5,*,ERR=1,END=9000) RF0,RFI0,RKX0,RKY0,RKZ0,RX0,RY0,RZ0
    IF(ABS(RF0).LE.1.D-8) THEN
       RF0=RF0_SAVE
       GOTO 9000
    END IF

    CRF=DCMPLX(RF1,RFI0)
    CKX=RKX0
    CKY=RKY0
    CKZ=RKZ0
    DO ns=1,nsmax
       MODELP_SAVE(ns)=MODELP(ns)
       MODELV_SAVE(ns)=MODELV(ns)
    END DO
    DO ns=1,nsmax
       MODELP(ns)=0
       MODELV(ns)=0
    END DO
    CD0=CFDISP(CRF,CKX,CKY,CKZ,RX0,RY0,RZ0)
    WRITE(6,'(A,1P2E16.8,A)') 'MODELP=0: (',CD0,')' 
    DO ns=1,nsmax
       MODELP(ns)=1
       MODELV(ns)=0
    END DO
    CD1=CFDISP(CRF,CKX,CKY,CKZ,RX0,RY0,RZ0)
    WRITE(6,'(A,1P2E16.8,A)') 'MODELP=1: (',CD1,')' 
    DO ns=1,nsmax
       MODELP(ns)=4
       MODELV(ns)=0
    END DO
    CD4=CFDISP(CRF,CKX,CKY,CKZ,RX0,RY0,RZ0)
    WRITE(6,'(A,1P2E16.8,A)') 'MODELP=4: (',CD4,')' 
    DO ns=1,nsmax
       MODELP(ns)=5
       MODELV(ns)=0
    END DO
    CD5=CFDISP(CRF,CKX,CKY,CKZ,RX0,RY0,RZ0)
    WRITE(6,'(A,1P2E16.8,A)') 'MODELP=5: (',CD5,')' 
    DO ns=1,nsmax
       MODELP(ns)=6
       MODELV(ns)=0
    END DO
    CD6=CFDISP(CRF,CKX,CKY,CKZ,RX0,RY0,RZ0)
    WRITE(6,'(A,1P2E16.8,A)') 'MODELP=6: (',CD6,')' 
    MODELV(1)=1
    DO ns=2,nsmax
       MODELP(ns)=6
       MODELV(ns)=0
    END DO
    CALL DPLDFM(1,0,IERR)
    CD2=CFDISP(CRF,CKX,CKY,CKZ,RX0,RY0,RZ0)
    WRITE(6,'(A,1P2E16.8,A)') 'MODELV=1: (',CD2,')' 
    MODELV(1)=3
    DO ns=2,nsmax
       MODELP(ns)=6
       MODELV(ns)=0
    END DO
    CALL DPLDFM(1,1,IERR)
    CD3=CFDISP(CRF,CKX,CKY,CKZ,RX0,RY0,RZ0)
    WRITE(6,'(A,1P2E16.8,A)') 'MODELV=3: (',CD3,')' 

    GOTO 1

9000 RETURN
  END SUBROUTINE DPGRP0

!     *************************
!           PLOT 1D GRAPH      
!     *************************

  SUBROUTINE DPGRP1

    USE dpcomm_local
    USE dpdisp
    IMPLICIT NONE
    REAL(4),DIMENSION(:),ALLOCATABLE:: GX,GY
    INTEGER:: ISW,NX
    REAL(rkind):: DRF
    COMPLEX(rkind):: CRF,CKX,CKY,CKZ,CD
    REAL(4):: GXMIN,GXMAX,GXSMN,GXSMX,GSCALX
    REAL(4):: GYMIN,GYMAX,GYSMN,GYSMX,GSCALY
    REAL(4):: GUCLIP
    REAL(rkind):: DRFI,RNPHII,DKX,DKY,DKZ

1   WRITE(6,*)'## SELECT : (1)RF (2)RFI (3)KX (4)KY (5)KZ (0)END'
    READ(5,*,ERR=1,END=9000) ISW
    IF(ISW.EQ.0) GOTO 9000

100 CONTINUE

    IF(ISW.EQ.1) THEN
2      WRITE(6,'(A,1P2E12.4,I5)') &
            ' INPUT : RF1,RF2,NGXMAX: ',RF1,RF2,NGXMAX
       READ(5,*,ERR=2,END=1) RF1,RF2,NGXMAX
       IF(NGXMAX.LE.0) GOTO 1
       ALLOCATE(GX(NGXMAX),GY(NGXMAX))
       DRF=(RF2-RF1)/NGXMAX
       DO NX=1,NGXMAX
          CRF=DCMPLX(RF1,RFI0)+DRF*(NX-1)
          CKX=RKX0
          CKY=RKY0
          CKZ=RKZ0
          CD=CFDISP(CRF,CKX,CKY,CKZ,RX0,RY0,RZ0)
          GX(NX)=GUCLIP(DBLE(CRF))
          GY(NX)=GUCLIP(LOG10(ABS(CD)))
       END DO
    ELSEIF(ISW.EQ.2) THEN
102    WRITE(6,'(A,1P2E12.4,I5)') &
            ' INPUT : RFI1,RFI2,NGXMAX: ',RFI1,RFI2,NGXMAX
       READ(5,*,ERR=102,END=1) RFI1,RFI2,NGXMAX
       IF(NGXMAX.LE.0) GOTO 1
       ALLOCATE(GX(NGXMAX),GY(NGXMAX))
       DRFI=(RFI2-RFI1)/NGXMAX
       DO NX=1,NGXMAX
          CRF=DCMPLX(RF0,RFI1)+DCMPLX(0.D0,DRFI)*(NX-1)
          CKX=RKX0
          CKY=RKY0
          CKZ=RKZ0
          CD=CFDISP(CRF,CKX,CKY,CKZ,RX0,RY0,RZ0)
          GX(NX)=GUCLIP(DIMAG(CRF))
          GY(NX)=GUCLIP(LOG10(ABS(CD)))
       END DO
    ELSEIF(ISW.EQ.3) THEN
202    WRITE(6,'(A,1P2E12.4,I5)') &
            ' INPUT : RKX1,RKX2,NGXMAX: ',RKX1,RKX2,NGXMAX, &
            '         RF0,RX0,RNPHII:   ',RF0,RX0,RNPHII
       READ(5,*,ERR=202,END=1) RKX1,RKX2,NGXMAX,RF0,RX0,RNPHII
       IF(NGXMAX.LE.0) GOTO 1
       ALLOCATE(GX(NGXMAX),GY(NGXMAX))
       DKX=(RKX2-RKX1)/NGXMAX
       DO NX=1,NGXMAX
          CRF=DCMPLX(RF0,RFI0)
          CKX=RKX1+DKX*(NX-1)
          RKY0=2.D6*PI*RF0*RNPHII/VC
          CKY=RKY0
          CKZ=RKZ0
          CD=CFDISP(CRF,CKX,CKY,CKZ,RX0,RY0,RZ0)
          GX(NX)=GUCLIP(DBLE(CKX))
          GY(NX)=GUCLIP(LOG10(ABS(CD)))
       END DO
    ELSEIF(ISW.EQ.4) THEN
302    WRITE(6,'(A,1P2E12.4,I5)') &
            ' INPUT : RKY1,RKY2,NGXMAX: ',RKY1,RKY2,NGXMAX
       READ(5,*,ERR=302,END=1) RKY1,RKY2,NGXMAX
       IF(NGXMAX.LE.0) GOTO 1
       ALLOCATE(GX(NGXMAX),GY(NGXMAX))
       DKY=(RKY2-RKY1)/NGXMAX
       DO NX=1,NGXMAX
          CRF=DCMPLX(RF0,RFI0)
          CKX=RKX0
          CKY=RKY1+DKY*(NX-1)
          CKZ=RKZ0
          CD=CFDISP(CRF,CKX,CKY,CKZ,RX0,RY0,RZ0)
          GX(NX)=GUCLIP(DBLE(CKY))
          GY(NX)=GUCLIP(LOG10(ABS(CD)))
       END DO
    ELSEIF(ISW.EQ.5) THEN
402    WRITE(6,'(A,1P2E12.4,I5)') &
            ' INPUT : RKZ1,RKZ2,NGXMAX: ',RKZ1,RKZ2,NGXMAX
       READ(5,*,ERR=402,END=1) RKZ1,RKZ2,NGXMAX
       IF(NGXMAX.LE.0) GOTO 1
       ALLOCATE(GX(NGXMAX),GY(NGXMAX))
       DKZ=(RKZ2-RKZ1)/NGXMAX
       DO NX=1,NGXMAX
          CRF=DCMPLX(RF0,RFI0)
          CKX=RKX0
          CKY=RKY0
          CKZ=RKZ1+DKZ*(NX-1)
          CD=CFDISP(CRF,CKX,CKY,CKZ,RX0,RY0,RZ0)
          GX(NX)=GUCLIP(DBLE(CKZ))
          GY(NX)=GUCLIP(LOG10(ABS(CD)))
       END DO
    ELSE
       GOTO 1
    ENDIF
    CALL PAGES
      CALL SETLIN(0,0,7)
      CALL SETCHS(0.3,0.)
      CALL GMNMX1(GX,1,NGXMAX,1,GXMIN,GXMAX)
      CALL GMNMX1(GY,1,NGXMAX,1,GYMIN,GYMAX)
      CALL GQSCAL(GXMIN,GXMAX,GXSMN,GXSMX,GSCALX)
      CALL GQSCAL(GYMIN,GYMAX,GYSMN,GYSMX,GSCALY)
      CALL GDEFIN(3.,18.,2.,17.,GXSMN,GXSMX,GYSMN,GYSMX)
      CALL GFRAME
      CALL GSCALE(GXSMN,GSCALX,0.0,0.0,0.2,9)
      CALL GSCALL(0.0,0,GYSMN,2,0.2,9)
      CALL GVALUE(GXSMN,2*GSCALX,0.0,0.0,-2)
      CALL GVALUL(0.0,0,GYSMN,1,0)
      CALL GPLOTP(GX,GY,1,NGXMAX,1,0,0,0)

      CALL MOVE(9.0,18.0)
      IF (ISW.EQ.1) THEN
       CALL TEXT (' RF ',4)
      ELSEIF (ISW.EQ.2) THEN
       CALL TEXT (' RFI',4) 
      ELSEIF (ISW.EQ.3) THEN
       CALL TEXT (' KPP',4) 
      ELSEIF (ISW.EQ.4) THEN
       CALL TEXT (' KPR',4) 
      ENDIF
      CALL MOVE(20.0,16.5)
      CALL TEXT('RF  =',5)
      CALL NUMBD(RF0,  '(1PE11.3)',11)
      CALL MOVE(20.0,16.0)
      CALL TEXT('RFI0=',5)
      CALL NUMBD(RFI0, '(1PE11.3)',11)
      CALL MOVE(20.0,15.5)
      CALL TEXT('KX  =',5)
      CALL NUMBD(RKX0,'(1PE11.3)',11)
      CALL MOVE(20.0,15.0)
      CALL TEXT('KY  =',5)
      CALL NUMBD(RKY0,'(1PE11.3)',11)
      CALL MOVE(20.0,14.5)
      CALL TEXT('KZ  =',5)
      CALL NUMBD(RKZ0,'(1PE11.3)',11)
    CALL PAGEE
    DEALLOCATE(GX,GY)
    GOTO 100

9000 RETURN
  END SUBROUTINE DPGRP1

!     *************************
!             FIND ROOT
!     *************************

  SUBROUTINE DP_ROOT

    USE dpcomm_local
    IMPLICIT NONE
    REAL(4),DIMENSION(:),ALLOCATABLE::  GX,GY,GY2
    REAL(4),DIMENSION(:,:),ALLOCATABLE:: GXA,GYA,GY2A
    INTEGER,DIMENSION(:),ALLOCATABLE::  NGXMAXA
    REAL(rkind):: RFX,RFIX,DKX,DKY,DKZ,DXPOS,RKXX,RKXIX
    COMPLEX(rkind):: CRF,CKX
    REAL(4):: GUCLIP
    INTEGER:: NX,ISW,I,IGMAX

    IGMAX=100

1   WRITE(6,*)'## SELECT : CRF : (1)ROOT (2)KX (3)KY (4)KZ (5) X'
    WRITE(6,*)'  (0) END : CKX : (6)ROOT (7)X1 (8)X2'
    READ(5,*,ERR=1,END=9000) ISW
    IF(ISW.EQ.0) GOTO 9000
    RFX=RF0
