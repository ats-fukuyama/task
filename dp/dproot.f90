    IF(ISW.EQ.1) THEN
2      WRITE(6,'(A,1P2E12.4)') ' INPUT : RFX,RFIX: ',RFX,RFIX
       READ(5,*,ERR=2,END=1) RFX,RFIX
       ILIST=1
       CKX0=RKX0
       CKY0=RKY0
       CKZ0=RKZ0
       XPOS0=RX0
       YPOS0=RY0
       ZPOS0=RZ0
       CRF=DCMPLX(RFX,RFIX)
       CALL DPBRENT1(CRF)
       WRITE(6,*) CRF
       GOTO 2
    ELSEIF(ISW.EQ.2) THEN
202    WRITE(6,'(A,1P2E12.4,I5)') &
            ' INPUT : RKX1,RKX2,NGXMAX: ',RKX1,RKX2,NGXMAX
       READ(5,*,ERR=202,END=1) RKX1,RKX2,NGXMAX
       ALLOCATE(GX(NGXMAX),GY(NGXMAX),GY2(NGXMAX))
       DKX=(RKX2-RKX1)/(NGXMAX-1)
       CRF=DCMPLX(RF0,RFI0)
       CKY0=RKY0
       CKZ0=RKZ0
       XPOS0=RX0
       YPOS0=RY0
       ZPOS0=RZ0
       DO NX=1,NGXMAX
          ILIST=0
          CKX0=RKX1+DKX*(NX-1)
          WRITE(6,*) CRF
          CALL DPBRENT1(CRF)
          GX(NX)=GUCLIP(DBLE(CKX0))
          GY(NX)=GUCLIP(DBLE(CRF))
          GY2(NX)=GUCLIP(DIMAG(-CRF))
       END DO
       CALL DPGR1A(GX,GY,GY2,NGXMAX,ISW)
    ELSEIF(ISW.EQ.3) THEN
302    WRITE(6,'(A,1P2E12.4,I5)') &
            ' INPUT : RKY1,RKY2,NGXMAX: ',RKY1,RKY2,NGXMAX
       READ(5,*,ERR=302,END=1) RKY1,RKY2,NGXMAX
       ALLOCATE(GX(NGXMAX),GY(NGXMAX),GY2(NGXMAX))
       DKY=(RKY2-RKY1)/(NGXMAX-1)
       CRF=DCMPLX(RF0,RFI0)
       CKX0=RKX0
       CKZ0=RKZ0
       XPOS0=RX0
       YPOS0=RY0
       ZPOS0=RZ0
       DO NX=1,NGXMAX
          ILIST=0
          CKY0=RKY1+DKY*(NX-1)
          CALL DPBRENT1(CRF)
          WRITE(6,*) CRF
          GX(NX)=GUCLIP(DBLE(CKY0))
          GY(NX)=GUCLIP(DBLE(CRF))
          GY2(NX)=GUCLIP(DIMAG(-CRF))
       END DO
       CALL DPGR1A(GX,GY,GY2,NGXMAX,ISW)
       DEALLOCATE(GX,GY,GY2)
    ELSEIF(ISW.EQ.4) THEN
402    WRITE(6,'(A,1P2E12.4,I5)') &
            ' INPUT : RKZ1,RKZ2,NGXMAX: ',RKZ1,RKZ2,NGXMAX
       READ(5,*,ERR=402,END=1) RKZ1,RKZ2,NGXMAX
       ALLOCATE(GX(NGXMAX),GY(NGXMAX),GY2(NGXMAX))
       DKZ=(RKZ2-RKZ1)/(NGXMAX-1)
       CRF=DCMPLX(RF0,RFI0)
       CKX0=RKX0
       CKY0=RKY0
       XPOS0=RX0
       YPOS0=RY0
       ZPOS0=RZ0
       DO NX=1,NGXMAX
          ILIST=0
          CKZ0=RKZ1+DKZ*(NX-1)
          CALL DPBRENT1(CRF)
          WRITE(6,*) CRF
          GX(NX)=GUCLIP(DBLE(CKZ0))
          GY(NX)=GUCLIP(DBLE(CRF))
          GY2(NX)=GUCLIP(DIMAG(-CRF))
       END DO
       CALL DPGR1A(GX,GY,GY2,NGXMAX,ISW)
       DEALLOCATE(GX,GY,GY2)
    ELSEIF(ISW.EQ.5) THEN
502    WRITE(6,'(A,1P2E12.4,I5)') &
            ' INPUT : RX1,RX2,NGXMAX: ',RX1,RX2,NGXMAX
       READ(5,*,ERR=502,END=1) RX1,RX2,NGXMAX
       ALLOCATE(GX(NGXMAX),GY(NGXMAX),GY2(NGXMAX))
       DXPOS=(RX2-RX1)/(NGXMAX-1)
       CRF=DCMPLX(RF0,RFI0)
       CKX0=RKX0
       CKY0=RKY0
       CKZ0=RKZ0
       YPOS0=RY0
       ZPOS0=RZ0
       DO NX=1,NGXMAX
          ILIST=0
          XPOS0=RX1+DXPOS*(NX-1)
          CALL DPBRENT1(CRF)
          WRITE(6,*) CRF
          GX(NX)=GUCLIP(XPOS0)
          GY(NX)=GUCLIP(DBLE(CRF))
          GY2(NX)=GUCLIP(DIMAG(-CRF))
       END DO
       CALL DPGR1A(GX,GY,GY2,NGXMAX,ISW)
       DEALLOCATE(GX,GY,GY2)
    ELSEIF(ISW.EQ.6) THEN
602    WRITE(6,'(A,1P2E12.4)') ' INPUT : RKX,RKXI: ',RKXX,RKXIX
       READ(5,*,ERR=602,END=1) RKXX,RKXIX
       ILIST=1
       CRF0=DCMPLX(RF0,0.D0)
       CKY0=RKY0
       CKZ0=RKZ0
       XPOS0=RX0
       YPOS0=RY0
       ZPOS0=RZ0
       CKX=DCMPLX(RKXX,RKXIX)
       CALL DPBRENT2(CKX)
       WRITE(6,*) CKX
       GOTO 602
    ELSEIF(ISW.EQ.7) THEN
702    WRITE(6,'(A,1P2E12.4,I5)') &
            ' INPUT : RX1,RX2,NGXMAX: ',RX1,RX2,NGXMAX
