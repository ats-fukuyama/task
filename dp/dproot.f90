!           1D plot
!     *******************************

  SUBROUTINE DPGR1A(GX,GY,GY2,NGXMAX,ISW)

      DIMENSION GX(NGXMAX),GY(NGXMAX),GY2(NGXMAX)

      CALL PAGES
      CALL SETLIN(0,0,7)
      CALL SETCHS(0.3,0.)
      CALL GMNMX1(GX,1,NGXMAX,1,GXMIN,GXMAX)
      CALL GMNMX1(GY,1,NGXMAX,1,GYMIN,GYMAX)
      CALL GQSCAL(GXMIN,GXMAX,GXSMN,GXSMX,GSCALX)
      CALL SETLIN(0,0,5)
      CALL GQSCAL(GYMIN,GYMAX,GYSMN,GYSMX,GSCALY)
      IF(GXMIN*GXMAX.LT.0.0) THEN
         GXORG=0.0
      ELSE
         GXORG=GXSMN
      ENDIF
      IF(GYMIN*GYMAX.LT.0.0) THEN
         GYORG=0.0
      ELSE
         GYORG=GYSMN
      ENDIF
      CALL GDEFIN(4.,17.,2.,17.,GXSMN,GXSMX,GYSMN,GYSMX)
      CALL SETLIN(0,0,7)
      CALL GFRAME
      CALL GSCALE(GXORG,GSCALX,0.0,0.0,0.2,9)
      CALL SETLIN(0,0,5)
      CALL GSCALE(0.0,0.0,GYORG,GSCALY,0.2,1)
      CALL GSCALE(0.0,0.0,0.0,10*(GYMAX-GYMIN),0.0,0)
      CALL SETLIN(0,0,7)
      CALL GVALUE(GXORG,2*GSCALX,0.0,0.0,2)
      CALL SETLIN(0,0,5)
      CALL GVALUE(0.0,0.0,GYORG,2*GSCALY,-2)
      CALL GPLOTP(GX,GY,1,NGXMAX,1,0,0,0)

      CALL SETLIN(0,0,2)
      CALL GMNMX1(GY2,1,NGXMAX,1,GYMIN,GYMAX)
      IF(GYMAX-GYMIN.GT.1.D-15) THEN
         CALL GQSCAL(GYMIN,GYMAX,GYSMN,GYSMX,GSCALY)
         IF(GYMIN*GYMAX.LT.0.0) THEN
            GYORG=0.0
         ELSE
            GYORG=GYSMN
         ENDIF
         CALL GDEFIN(4.,17.,2.,17.,GXSMN,GXSMX,GYSMN,GYSMX)
         CALL GSCALE(0.0,0.0,GYORG,GSCALY,0.2,5)
         CALL GVALUE(0.0,0.0,GYORG,2*GSCALY,-402)
         CALL GVALUE(0.0,0.0,GYORG,2*GSCALY,-402)
         CALL GPLOTP(GX,GY2,1,NGXMAX,1,0,0,2)
      ENDIF

      CALL DPGPRM(ISW)
      CALL PAGEE
      RETURN
  END SUBROUTINE DPGR1A

!     *******************************
!           1D plot
!     *******************************

  SUBROUTINE DPGR1B(GXA,GYA,GY2A,NXM,NGXMAXA,IGMAX,ISW)

      DIMENSION GXA(NXM,IGMAX),GYA(NXM,IGMAX),GY2A(NXM,IGMAX)
      DIMENSION NGXMAXA(IGMAX)

      CALL PAGES
      CALL SETLIN(0,0,7)
      CALL SETCHS(0.3,0.)
      GXMIN  = 1.E20
      GXMAX  =-1.E20
      GYMIN  = 1.E20
      GYMAX  =-1.E20
      GYMIN3 = 1.E20
      GYMAX3 =-1.E20
      DO IG=1,IGMAX
         CALL GMNMX1(GXA(1,IG),1,NGXMAXA(IG),1,GXMIN1,GXMAX1)
         CALL GMNMX1(GYA(1,IG),1,NGXMAXA(IG),1,GYMIN1,GYMAX1)
         CALL GMNMX1(GY2A(1,IG),1,NGXMAXA(IG),1,GYMIN2,GYMAX2)
         GXMIN  = MIN(GXMIN,GXMIN1)
         GXMAX  = MAX(GXMAX,GXMAX1)
         GYMIN  = MIN(GYMIN,GYMIN1)
         GYMAX  = MAX(GYMAX,GYMAX1)
         GYMIN3 = MIN(GYMIN3,GYMIN2)
         GYMAX3 = MAX(GYMAX3,GYMAX2)
      END DO
      GYMIN  = MIN(GYMIN,GYMIN3)
      GYMAX  = MAX(GYMAX,GYMAX3)
      CALL GQSCAL(GXMIN,GXMAX,GXSMN,GXSMX,GSCALX)
      CALL GQSCAL(GYMIN,GYMAX,GYSMN,GYSMX,GSCALY)
      IF(GXMIN*GXMAX.LT.0.0) THEN
         GXORG=0.0
      ELSE
         GXORG=GXSMN
      ENDIF
      IF(GYMIN*GYMAX.LT.0.0) THEN
         GYORG=0.0
      ELSE
         GYORG=GYSMN
      ENDIF
      CALL GDEFIN(4.,17.,2.,17.,GXSMN,GXSMX,GYSMN,GYSMX)
      CALL GFRAME
      CALL GSCALE(GXORG,GSCALX,0.0,0.0,0.2,9)
      CALL GSCALE(0.0,0.0,GYORG,GSCALY,0.2,9)
      CALL GSCALE(0.0,0.0,0.0,10*(GYMAX-GYMIN),0.0,0)
      CALL GVALUE(GXORG,2*GSCALX,0.0,0.0,2)
      CALL GVALUE(0.0,0.0,GYORG,2*GSCALY,-2)
      DO IG=1,IGMAX
         CALL SETLIN(0,0,4)
         CALL GPLOTP(GXA(1,IG),GYA(1,IG),1,NGXMAXA(IG),1,0,0,0)
         CALL SETLIN(0,0,6)
         CALL GPLOTP(GXA(1,IG),GY2A(1,IG),1,NGXMAXA(IG),1,0,0,1)
      END DO

      CALL DPGPRM(ISW)
      CALL PAGEE
      RETURN
  END SUBROUTINE DPGR1B

