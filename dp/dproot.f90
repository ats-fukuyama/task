       ALLOCATE(NGXMAXA(IGMAX))
       I=0
802    WRITE(6,'(A,1P2E12.4,I5,1PE12.4)') &
            ' INPUT : RX1,RX2,NGXMAX,RKX0: ',RX1,RX2,NGXMAX,RKX0
       READ(5,*,ERR=802,END=8000) RX1,RX2,NGXMAX,RKX0
       IF(RX1.EQ.RX2)   GO TO 8000
       I=I+1
       DXPOS=(RX2-RX1)/(NGXMAX-1)
       CRF0=DCMPLX(RF0,0.D0)
       CKX=DCMPLX(RKX0,0.D0)
       CKY0=RKY0
       CKZ0=RKZ0
       YPOS0=RY0
       ZPOS0=RZ0
       NGXMAXA(I)=NGXMAX
       DO NX=1,NGXMAX
          ILIST=0
          XPOS0=RX1+DXPOS*(NX-1)
          CALL DPBRENT2(CKX)
          GXA(NX,I)=GUCLIP(XPOS0)
          IF(DBLE(CKX**2).GT.0) THEN
             GYA(NX,I)=GUCLIP(SQRT(DBLE(CKX**2)))
          ELSEIF(DBLE(CKX**2).LT.0) THEN         
             GYA(NX,I)=GUCLIP(-SQRT(-DBLE(CKX**2)))
          ENDIF
          IF(DIMAG(CKX**2).GT.0) THEN
             GY2A(NX,I)=GUCLIP(SQRT(DIMAG(CKX**2)))
          ELSEIF(DIMAG(CKX**2).LT.0) THEN         
             GY2A(NX,I)=GUCLIP(-SQRT(DIMAG(CKX**2)))
          ENDIF
          WRITE(6,*) CKX
       END DO
       CALL DPGR1A(GXA(1,I),GYA(1,I),GY2A(1,I),NGXMAX,ISW)
       IF(I.LT.IGMAX) GOTO 802
8000   CONTINUE
       CALL DPGR1B(GXA,GYA,GY2A,NGXMAX,NGXMAXA,I,ISW)
       DEALLOCATE(GXA,GYA,GY2A,NGXMAXA)
    ENDIF

   GOTO 1

9000 RETURN
 END SUBROUTINE DP_ROOT

!     *************************
!           BRENT METHOD 1
!     *************************

  SUBROUTINE DPBRENT1(CX)

    USE dpcomm_local
    IMPLICIT NONE
    COMPLEX(rkind),INTENT(INOUT):: CX
    REAL(rkind),DIMENSION(:),ALLOCATABLE:: X,F,WA
    INTEGER:: M,NBRMAX,INFO,LWA

    M = LMAXRT
    NBRMAX = 2
    LWA=NBRMAX*(NBRMAX+3)
    ALLOCATE(X(NBRMAX),F(NBRMAX),WA(LWA))
    X(1)=DBLE(CX)
    X(2)=DIMAG(CX)

    CALL FBRENTN(DPFUNC1,NBRMAX,X,F,EPSRT,INFO,WA,LWA)
    IF(INFO.GE.1.AND.INFO.LE.3) THEN
       X(1)=RF0
       X(2)=RFI0
    ENDIF

    CX=DCMPLX(X(1),X(2))
    DEALLOCATE(X,F,WA)
    RETURN
 END SUBROUTINE DPBRENT1

!     *******************************
!           Determinant Function
!     *******************************

  SUBROUTINE DPFUNC1(N,X,F,ID)

    USE dpcomm_local
    USE dpdisp
    IMPLICIT NONE
    INTEGER,INTENT(IN):: N,ID
    REAL(rkind),INTENT(IN):: X(N)
    REAL(rkind),INTENT(INOUT):: F(N)
    COMPLEX(rkind):: CRF,CFUNC

    CRF=DCMPLX(X(1),X(2))
    CFUNC=CFDISP(CRF,CKX0,CKY0,CKZ0,XPOS0,YPOS0,ZPOS0)
    IF(ID.EQ.1)THEN
       F(1)=DBLE(CFUNC)
    ELSE
       F(2)=DIMAG(CFUNC)
    END IF
    IF(ILIST.NE.0) WRITE(6,'(1P3E12.4,I5)') X(1),X(2),F(ID),ID
    RETURN
  END SUBROUTINE DPFUNC1

!     *******************************
!           BRENT METHOD 2
!     *******************************

  SUBROUTINE DPBRENT2(CX)

    USE dpcomm_local
    IMPLICIT NONE
    COMPLEX(rkind),INTENT(INOUT):: CX
    REAL(rkind),DIMENSION(:),ALLOCATABLE:: X,F,WA
    INTEGER:: M,NBRMAX,LWA,INFO
    REAL(rkind):: EPS

    EPS  = 1.D2
    M = 100
    NBRMAX=2
    LWA=NBRMAX*(NBRMAX+3)
    ALLOCATE(X(NBRMAX),F(NBRMAX),WA(LWA))

    X(1)=DBLE(CX)
    X(2)=DIMAG(CX)

    CALL FBRENTN(DPFUNC2,NBRMAX,X,F,EPS,INFO,WA,LWA)
    IF(INFO.GE.1.AND.INFO.LE.3) THEN
       X(1)=RKX0
       X(2)=0.D0
    ENDIF

    CX=DCMPLX(X(1),X(2))
    DEALLOCATE(X,F,WA)
    RETURN
  END SUBROUTINE DPBRENT2

!     *******************************
!           Determinant Function
!     *******************************

  SUBROUTINE DPFUNC2(N,X,F,I)

    USE dpcomm_local
    USE dpdisp
    IMPLICIT NONE

    INTEGER,INTENT(IN):: N,I
    REAL(rkind),INTENT(IN):: X(N)
    REAL(rkind),INTENT(OUT):: F(N)
    COMPLEX(rkind):: CKX,CFUNC2

    CKX=DCMPLX(X(1),X(2))
    CFUNC2=CFDISP(CRF0,CKX,CKY0,CKZ0,XPOS0,YPOS0,ZPOS0)
    IF(I.EQ.1)THEN
       F(1)=DBLE(CFUNC2)
    ELSE
       F(2)=DIMAG(CFUNC2)
    END IF
    IF(ILIST.NE.0) WRITE(6,'(1P3E12.4,I5)') X(1),X(2),F(I),I
    RETURN
  END SUBROUTINE DPFUNC2

